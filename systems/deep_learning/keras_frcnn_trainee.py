class KerasFRCNNTrainee(core.trained_system.VisionSystemTrainee):
    """
    A class for training FRCNN based on Keras.

    A trainee class for building FRCNNs using Keras.
    Based on train_frcnn.py from keras_frcnn

    """

    def __init__(self, weights_file, classes, num_rois=32, input_weight_path=None, use_training_loss=False,
                 self_assessment_interval=1000, id_=None, **kwargs):
        """

        :param weights_file: The file path to store the weights in. Call generateFilename to make one.
        :param classes: A set of lowercase class names to try and detect
        :param num_rois: Number of ROIs per iteration. Higher means more memory use.
        :param horizontal_flips: Augment with horizontal flips in training. (Default=false).
        :param vertical_flips: Augment with vertical flips in training. (Default=false).
        :param rot_90: Augment with 90 degree rotations in training. (Default=false).
        :param num_epochs: Number of epochs.
        :param input_weight_path: Input path for weights. If not specified, will use default weights provided by keras.
        :param use_training_loss: Should we update the trained model based on the training loss, as well as validation
        :param self_assessment_interval: The frequency with which we evaluate the training loss, if necessary
        :param id_:
        :param kwargs:
        """
        super().__init__(id_=id_, **kwargs)
        self._weights_filename = str(weights_file)
        self._num_rois = int(num_rois)
        self._initial_weights_path = input_weight_path if os.path.isfile(input_weight_path) else None
        self._self_assessment_interval = int(self_assessment_interval) if bool(use_training_loss) else -1
        # TODO: There are more options in config that affect the network structure, add parameters?

        # We need to fix the classes at configure time, they are used to shape the network
        self._class_mapping = {}
        classes = {cls.lower() for cls in classes}
        for idx, cls in enumerate(classes):
            self._class_mapping[cls] = idx
        if 'bg' not in classes:
            idx = len(self._class_mapping)
            self._class_mapping['bg'] = idx

        # Training state
        self._config = None
        self._model_rpn = None
        self._model_classifier = None
        self._model_all = None
        self._training_losses = None
        self._best_training_loss = None
        self._validation_losses = None
        self._validation_count = None
        self._best_validation_loss = None
        self._image_counter = None

    @property
    def required_training_type(self):
        """
        FRCNN requires non-sequential training
        :return: NON_SEQUENTIAL
        :rtype core.sequence_type.ImageSequenceType:
        """
        return core.sequence_type.ImageSequenceType.NON_SEQUENTIAL

    def is_image_source_appropriate(self, image_source):
        """
        Is the dataset appropriate for training this vision system.
        FRCNN requires bounding boxes, but not any more detailed labels

        :param image_source: The source for images
        :return: True if the source has bounding boxes
        :rtype: bool
        """
        return image_source.is_labels_available

    def start_training(self, num_images=None):
        """
        Start training FRCNN
        :return: void
        """
        self._config = config.Config()
        self._config.num_rois = int(self._num_rois)
        self._config.output_weight_path = self._weights_filename
        self._config.class_mapping = self._class_mapping

        if self._initial_weights_path is not None:
            self._config.base_net_weights = self._initial_weights_path

        # Define the networks
        if keras_backend.image_dim_ordering() == 'th':
            input_shape_img = (3, None, None)
        else:
            input_shape_img = (None, None, 3)

        img_input = Input(shape=input_shape_img)
        roi_input = Input(shape=(self._config.num_rois, 4))

        # define the base network (resnet here, can be VGG, Inception, etc)
        shared_layers = nn.nn_base(img_input, trainable=True)

        # define the RPN, built on the base layers
        # TODO: These parameters are never set, uses defaults from keras_frcnn.config
        num_anchors = len(self._config.anchor_box_scales) * len(self._config.anchor_box_ratios)
        rpn = nn.rpn(shared_layers, num_anchors)

        classifier = nn.classifier(shared_layers, roi_input, self._config.num_rois,
                                   nb_classes=len(self._class_mapping), trainable=True)

        self._model_rpn = Model(img_input, rpn[:2])
        self._model_classifier = Model([img_input, roi_input], classifier)

        # this is a model that holds both the RPN and the classifier, used to load/save weights for the models
        self._model_all = Model([img_input, roi_input], rpn[:2] + classifier)

        # Load pre-trained weights from a file
        # Weights can be found at:
        # https://github.com/fchollet/deep-learning-models/releases/download/v0.2/resnet50_weights_th_dim_ordering_th_kernels_notop.h5
        # https://github.com/fchollet/deep-learning-models/releases/download/v0.2/resnet50_weights_tf_dim_ordering_tf_kernels_notop.h5
        if self._initial_weights_path is not None:
            self._model_rpn.load_weights(self._initial_weights_path, by_name=True)
            self._model_classifier.load_weights(self._initial_weights_path, by_name=True)

        optimizer = Adam(lr=1e-4)
        optimizer_classifier = Adam(lr=1e-4)
        self._model_rpn.compile(optimizer=optimizer,
                                loss=[frcnn_losses.rpn_loss_cls(num_anchors), frcnn_losses.rpn_loss_regr(num_anchors)])
        self._model_classifier.compile(optimizer=optimizer_classifier,
                                       loss=[frcnn_losses.class_loss_cls,
                                             frcnn_losses.class_loss_regr(len(self._class_mapping) - 1)],
                                       metrics={'dense_class_{}'.format(len(self._class_mapping)): 'accuracy'})
        self._model_all.compile(optimizer='sgd', loss='mae')

        self._best_validation_loss = np.Inf
        self._image_counter = 0
        if self._self_assessment_interval > 0:
            # If we want to measure the training loss, build a buffer for the losses, and remember the best
            self._training_losses = np.zeros((self._self_assessment_interval, 4))
            self._best_training_loss = np.Inf

    def train_with_image(self, image, index=None):
        """
        Train the system with a particular image.
        This should be called with
        :param image: The image object
        :param index: The index number of the training image.
        :return: void
        """
        img_data, gt_regions, img_metadata = preprocess_image(image, self._config)
        if img_data is None:
            # Image is not valid, return
            return

        loss_rpn = self._model_rpn.train_on_batch(img_data, gt_regions)

        prob_rpn = self._model_rpn.predict_on_batch(img_data)

        regions = roi_helpers.rpn_to_roi(prob_rpn[0], prob_rpn[1], self._config, keras_backend.image_dim_ordering(),
                                         use_regr=True, overlap_thresh=0.7, max_boxes=300)

        # note: calc_iou converts from (x1,y1,x2,y2) to (x,y,w,h) format
        X2, Y1, Y2 = roi_helpers.calc_iou(regions, img_metadata, self._config, self._class_mapping)

        if X2 is None:
            return

        neg_samples = np.where(Y1[0, :, -1] == 1)
        pos_samples = np.where(Y1[0, :, -1] == 0)

        if len(neg_samples) > 0:
            neg_samples = neg_samples[0]
        else:
            neg_samples = []

        if len(pos_samples) > 0:
            pos_samples = pos_samples[0]
        else:
            pos_samples = []

        # Choose a 50/50 mix if positive and negative samples to train the classifier
        if self._config.num_rois > 1:
            if len(pos_samples) < self._config.num_rois // 2:
                selected_pos_samples = pos_samples.tolist()
            else:
                selected_pos_samples = np.random.choice(pos_samples, self._config.num_rois // 2, replace=False).tolist()
            if len(neg_samples) >= self._config.num_rois - len(selected_pos_samples):
                selected_neg_samples = np.random.choice(neg_samples, self._config.num_rois - len(selected_pos_samples),
                                                        replace=False).tolist()
            else:
                selected_neg_samples = np.random.choice(neg_samples, self._config.num_rois - len(selected_pos_samples),
                                                        replace=True).tolist()

            sel_samples = selected_pos_samples + selected_neg_samples
        else:
            # in the extreme case where num_rois = 1, we pick a random pos or neg sample
            if np.random.randint(0, 2):
                sel_samples = random.choice(neg_samples)
            else:
                sel_samples = random.choice(pos_samples)

        loss_class = self._model_classifier.train_on_batch([img_data, X2[:, sel_samples, :]],
                                                           [Y1[:, sel_samples, :], Y2[:, sel_samples, :]])

        if self._self_assessment_interval > 0:
            iter_num = self._image_counter % len(self._training_losses)
            self._training_losses[iter_num, 0] = loss_rpn[1]
            self._training_losses[iter_num, 1] = loss_rpn[2]

            self._training_losses[iter_num, 2] = loss_class[1]
            self._training_losses[iter_num, 3] = loss_class[2]

            if iter_num == 0 and self._image_counter > 0:
                loss_rpn_cls = np.mean(self._training_losses[:, 0])
                loss_rpn_regr = np.mean(self._training_losses[:, 1])
                loss_class_cls = np.mean(self._training_losses[:, 2])
                loss_class_regr = np.mean(self._training_losses[:, 3])

                curr_loss = loss_rpn_cls + loss_rpn_regr + loss_class_cls + loss_class_regr
                if curr_loss < self._best_training_loss:
                    # Save, if we have improved the training loss
                    self._best_training_loss = curr_loss
                    self._model_all.save_weights(self._config.model_path)

        # At the end, increment the count of images we've seen to maintain zero-indexing
        self._image_counter += 1

    def start_validation(self, num_validation_images=None):
        """
        While training, start performing validation
        This is done to reduce overfitting
        :param num_validation_images:
        :return:
        """
        if num_validation_images is None:
            num_validation_images = 100
        self._validation_count = 0
        self._validation_losses = np.zeros((num_validation_images, 4))

    def validate_with_image(self, image):
        """
        Measure performance with a particular image. This should accumulate during validation.
        :param image: The image
        :return:
        """
        img_data, gt_regions, img_metadata = preprocess_image(image, self._config)
        if img_data is None:
            # Image is not valid, return
            return

        loss_rpn = self._model_rpn.test_on_batch(img_data, gt_regions)

        prob_rpn = self._model_rpn.predict_on_batch(img_data)

        regions = roi_helpers.rpn_to_roi(prob_rpn[0], prob_rpn[1], self._config, keras_backend.image_dim_ordering(),
                                         use_regr=True, overlap_thresh=0.7, max_boxes=300)

        # note: calc_iou converts from (x1,y1,x2,y2) to (x,y,w,h) format
        X2, Y1, Y2 = roi_helpers.calc_iou(regions, img_metadata, self._config, self._class_mapping)

        if X2 is None:
            return

        neg_samples = np.where(Y1[0, :, -1] == 1)
        pos_samples = np.where(Y1[0, :, -1] == 0)

        if len(neg_samples) > 0:
            neg_samples = neg_samples[0]
        else:
            neg_samples = []

        if len(pos_samples) > 0:
            pos_samples = pos_samples[0]
        else:
            pos_samples = []

        # Choose a 50/50 mix if positive and negative samples to train the classifier
        if self._config.num_rois > 1:
            if len(pos_samples) < self._config.num_rois // 2:
                selected_pos_samples = pos_samples.tolist()
            else:
                selected_pos_samples = np.random.choice(pos_samples, self._config.num_rois // 2, replace=False).tolist()
            if len(neg_samples) >= self._config.num_rois - len(selected_pos_samples):
                selected_neg_samples = np.random.choice(neg_samples, self._config.num_rois - len(selected_pos_samples),
                                                        replace=False).tolist()
            else:
                selected_neg_samples = np.random.choice(neg_samples, self._config.num_rois - len(selected_pos_samples),
                                                        replace=True).tolist()

            sel_samples = selected_pos_samples + selected_neg_samples
        else:
            # in the extreme case where num_rois = 1, we pick a random pos or neg sample
            if np.random.randint(0, 2):
                sel_samples = random.choice(neg_samples)
            else:
                sel_samples = random.choice(pos_samples)

        loss_class = self._model_classifier.test_on_batch([img_data, X2[:, sel_samples, :]],
                                                          [Y1[:, sel_samples, :], Y2[:, sel_samples, :]])
        # TODO: Widen the validation losses if we have to
        self._validation_losses[self._validation_count, :] = loss_rpn[1], loss_rpn[2], loss_class[1], loss_class[2]

    def finish_validation(self):
        """
        Finish validation and return to training.
        This checks the validation set performance, and saves the model if there is an improvement
        :return:
        """
        loss_rpn_cls = np.mean(self._validation_losses[0:self._validation_count, 0])
        loss_rpn_regr = np.mean(self._validation_losses[0:self._validation_count, 1])
        loss_class_cls = np.mean(self._validation_losses[0:self._validation_count, 2])
        loss_class_regr = np.mean(self._validation_losses[0:self._validation_count, 3])

        curr_loss = loss_rpn_cls + loss_rpn_regr + loss_class_cls + loss_class_regr
        if curr_loss < self._best_validation_loss:
            # Save, if we have improved the validation loss
            self._best_validation_loss = curr_loss
            self._model_all.save_weights(self._config.model_path)
        self._validation_losses = None

    def finish_training(self, trainer_id, image_source_ids=None, training_settings=None):
        """
        Finish training, producing a new vision system instance,
        which will be saved in the database as a new vision system to be tested.

        If the training method is not sequential, requiring all images to train,
        then train_with_image should simply accumulate images, and this method
        should actually perform the training.

        :return: TrainedVisionSystem
        """
        if training_settings is not None:
            self._config.use_horizontal_flips = bool(training_settings['horizontal_flips'])
            self._config.use_vertical_flips = bool(training_settings['vertical_flips'])
            self._config.rot_90 = bool(training_settings['rot_90'])
        trained_system = systems.deep_learning.keras_frcnn.KerasFRCNN(
            config=self._config, num_rois=self._num_rois,
            vision_system_trainer=trainer_id,
            training_image_source_ids=image_source_ids,
            training_settings=training_settings)
        # Clear training state, ready to train again.
        self._config = None
        return trained_system

    def serialize(self):
        """
        Serialize the trainer
        :return: a dictionary representation of the entity, that can be saved to MongoDB
        """
        serialized = super().serialize()
        serialized['weights_filename'] = self._weights_filename
        serialized['num_rois'] = self._num_rois
        serialized['initial_weights_path'] = self._initial_weights_path
        serialized['self_assessment_interval'] = self._self_assessment_interval
        serialized['class_mapping'] = self._class_mapping
        return serialized

    @classmethod
    def deserialize(cls, serialized_representation, db_client, **kwargs):
        if 'weights_filename' in serialized_representation:
            kwargs['weights_file'] = serialized_representation['weights_filename']
        if 'num_rois' in serialized_representation:
            kwargs['num_rois'] = serialized_representation['num_rois']
        if 'initial_weights_path' in serialized_representation:
            kwargs['input_weight_path'] = serialized_representation['initial_weights_path']
        if 'self_assessment_interval' in serialized_representation:
            kwargs['use_training_loss'] = bool(serialized_representation['self_assessment_interval'] > 0)
            kwargs['self_assessment_interval'] = serialized_representation['self_assessment_interval']
        if 'class_mapping' in serialized_representation:
            kwargs['classes'] = serialized_representation['class_mapping']
        return super().deserialize(serialized_representation, db_client, **kwargs)


def generate_filename(folder):
    index = 0
    # TODO: Human readable name
    base_filename = os.path.join(folder, "weights-{0}".format(str(datetime.datetime.now()).replace(' ', '-')))
    filename = base_filename + '.h5'
    while os.path.isfile(filename) and index < 100000000:
        index += 1
        filename = base_filename + "-{0}.h5".format(index)
    return filename


def preprocess_image(image, config):
    # Deep-copy the image, changing data type to float
    x_img = np.array(image.data, copy=True, dtype=np.float32)

    rows, cols, _ = x_img.shape
    width, height = cols, rows

    # get image dimensions for resizing
    resized_width, resized_height = data_generators.get_new_img_size(width, height, config.im_size)

    # resize the image so that smalles side is length = 600px
    x_img = cv2.resize(x_img, (resized_width, resized_height), interpolation=cv2.INTER_CUBIC)

    # Build the bounding boxes in the format keras_frcnn expects
    # We filter out bounding boxes for classes we don't care about
    bounding_boxes = {
        'bboxes': [{
            'x1': obj.bounding_box[0],
            'y1': obj.bounding_box[1],
            'x2': obj.bounding_box[0] + obj.bounding_box[2],
            'y2': obj.bounding_box[1] + obj.bounding_box[3]
        } for obj in image.metadata.labelled_objects
            if len(obj.class_names & set(config.class_mapping.keys())) > 0]
    }
    try:
        y_rpn_cls, y_rpn_regr = data_generators.calc_rpn(config, bounding_boxes, width, height,
                                                         resized_width, resized_height, nn.get_img_output_length)
    except:
        return None, None, None

    # Zero-center by mean pixel, and preprocess image
    x_img[:, :, 0] -= config.img_channel_mean[0]
    x_img[:, :, 1] -= config.img_channel_mean[1]
    x_img[:, :, 2] -= config.img_channel_mean[2]
    x_img /= config.img_scaling_factor

    x_img = np.transpose(x_img, (2, 0, 1))
    x_img = np.expand_dims(x_img, axis=0)

    y_rpn_regr[:, y_rpn_regr.shape[1] // 2:, :, :] *= config.std_scaling

    if keras_backend.image_dim_ordering() == 'tf':
        x_img = np.transpose(x_img, (0, 2, 3, 1))
        y_rpn_cls = np.transpose(y_rpn_cls, (0, 2, 3, 1))
        y_rpn_regr = np.transpose(y_rpn_regr, (0, 2, 3, 1))

    return np.copy(x_img), [np.copy(y_rpn_cls), np.copy(y_rpn_regr)], {
        'width': width,
        'height': height,
        'bboxes': bounding_boxes
    }
