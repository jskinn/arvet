import os.path
import cv2
import numpy as np
import keras.backend as keras_backend
from keras.layers import Input
from keras.models import Model
import keras_frcnn.roi_helpers as roi_helpers
import keras_frcnn.config
import keras_frcnn.resnet as network
import core.sequence_type
import core.system
import trials.object_detection.bounding_box_result as bbox_result
import systems.deep_learning.config_helpers as config_help


class KerasFRCNN(core.system.VisionSystem):
    """
    A Vision system for testing a keras-based FRCNN that does bounding-box estimation
    Based on keras-frcnn, found here: https://github.com/yhenon/keras-frcnn.
    This file is derived from test_frcnn.py and train_frcnn.py in the above project.
    """

    def __init__(self, config, num_rois=32, id_=None):
        super().__init__(id_=id_)

        if not isinstance(config, keras_frcnn.config.Config):
            raise ValueError("Config parameter must be a keras_frcnn configuration produced during training."
                             " Train a model or unpickle an existing one.")

        # Config
        self._config = config
        self._config.num_rois = int(num_rois)
        self._bbox_threshold = 0.8      # Configurable, perhaps?

        # No data augmentation when testing
        self._use_horizontal_flips = self._config.use_horizontal_flips
        self._use_vertical_flips = self._config.use_vertical_flips
        self._rot_90 = self._config.rot_90
        self._config.use_horizontal_flips = False
        self._config.use_vertical_flips = False
        self._config.rot_90 = False

        # Trial state
        self._gt_bounding_boxes = None
        self._bounding_boxes = None
        self._model_rpn = None
        self._model_classifier_only = None
        self._sequence_type = None
        self._class_mapping = {}

    @property
    def num_regions_of_interest(self):
        return self._config.num_rois

    @num_regions_of_interest.setter
    def num_regions_of_interest(self, num_rois):
        if not self.is_trial_running():
            self._config.num_rois = int(num_rois)

    def is_trial_running(self):
        """
        Has the trial started.
        Don't change the config when the trial is running
        :return:
        """
        return self._bounding_boxes is not None

    @property
    def is_deterministic(self):
        """
        Is the visual system deterministic.
        :return: True, features are deterministic
        :rtype: bool
        """
        return True

    def is_image_source_appropriate(self, image_source):
        """
        Is the dataset appropriate for testing this vision system.
        Requirements are that we have ground-truth labels and that we can match back to the image ids.
        TODO: Copy information from the image, so we don't have to index back to it.
        :param image_source: The source for images that this system will potentially be run with.
        :return: True iff the particular dataset is appropriate for this vision system.
        :rtype: bool
        """
        return image_source.is_stored_in_database and image_source.is_labels_available

    def start_trial(self, sequence_type):
        """
        Start a trial for a feature detector
        :return: void
        """

        # TODO: Maybe need to set the recursion limit? Look for crashes if this is missing
        # self._old_recusion_limit = sys.getrecursionlimit()
        # if self._old_recursion_limit < 40000:
        #     sys.setrecursionlimit(40000)

        # Set up a mapping between integer ids and class names
        class_mapping = self._config.class_mapping
        if 'bg' not in class_mapping:
            class_mapping['bg'] = len(class_mapping)
        self._class_mapping = {v: k for k, v in class_mapping.items()}

        # Construct the network
        if keras_backend.image_dim_ordering() == 'th':
            input_shape_img = (3, None, None)
            input_shape_features = (1024, None, None)
        else:
            input_shape_img = (None, None, 3)
            input_shape_features = (None, None, 1024)

        img_input = Input(shape=input_shape_img)
        roi_input = Input(shape=(self._config.num_rois, 4))
        feature_map_input = Input(shape=input_shape_features)

        # define the base network (resnet here, can be VGG, Inception, etc)
        shared_layers = network.nn_base(img_input, trainable=True)

        # define the RPN, built on the base layers
        num_anchors = len(self._config.anchor_box_scales) * len(self._config.anchor_box_ratios)
        rpn_layers = network.rpn(shared_layers, num_anchors)

        classifier = network.classifier(feature_map_input, roi_input, self._config.num_rois,
                                        nb_classes=len(class_mapping), trainable=True)

        self._model_rpn = Model(img_input, rpn_layers)
        self._model_classifier_only = Model([feature_map_input, roi_input], classifier)

        model_classifier = Model([feature_map_input, roi_input], classifier)

        self._model_rpn.load_weights(self._config.model_path, by_name=True)
        model_classifier.load_weights(self._config.model_path, by_name=True)

        self._model_rpn.compile(optimizer='sgd', loss='mse')
        model_classifier.compile(optimizer='sgd', loss='mse')

        # Record the results
        self._sequence_type = core.sequence_type.ImageSequenceType(sequence_type)
        self._bounding_boxes = {}
        self._gt_bounding_boxes = {}

    def process_image(self, image, timestamp):
        """
        Process an image as part of the current run.
        :param image: An Image object
        :param timestamp: The timestamp or index of the image in this collection
        :return: void
        """
        if not self.is_trial_running():
            self.start_trial(core.sequence_type.ImageSequenceType.NON_SEQUENTIAL)

        # Convert the input image to be fed into the network
        formatted_image, aspect_ratio = format_img(image.data[:, :, ::-1], self._config)
        if keras_backend.image_dim_ordering() == 'tf':
            formatted_image = np.transpose(formatted_image, (0, 2, 3, 1))

        # get the feature maps and output from the RPN (Region Proposal Network)
        [rpn_class, rpn_regr, shared_layers_output] = self._model_rpn.predict(formatted_image)

        # Convert region proposals to bounding boxes
        result = roi_helpers.rpn_to_roi(rpn_class, rpn_regr, self._config, keras_backend.image_dim_ordering(),
                                        overlap_thresh=0.7)

        # convert from (x1,y1,x2,y2) to (x,y,w,h)
        result[:, 2] -= result[:, 0]
        result[:, 3] -= result[:, 1]

        # apply the spatial pyramid pooling to the proposed regions
        bboxes = {}
        probs = {}
        for jk in range(result.shape[0] // self._config.num_rois + 1):  # // indicates integer division
            regions_of_interest = np.expand_dims(result[self._config.num_rois * jk:
                                                        self._config.num_rois * (jk + 1), :], axis=0)
            if regions_of_interest.shape[1] == 0:
                break

            if jk == result.shape[0] // self._config.num_rois:
                # pad result
                curr_shape = regions_of_interest.shape
                target_shape = (curr_shape[0], self._config.num_rois, curr_shape[2])
                rois_padded = np.zeros(target_shape).astype(regions_of_interest.dtype)
                rois_padded[:, :curr_shape[1], :] = regions_of_interest
                rois_padded[0, curr_shape[1]:, :] = regions_of_interest[0, 0, :]
                regions_of_interest = rois_padded

            [prob_class, prob_regr] = self._model_classifier_only.predict([shared_layers_output, regions_of_interest])

            for ii in range(prob_class.shape[1]):

                # If our most likely class is less than our threshold, or is background
                if (np.max(prob_class[0, ii, :]) < self._bbox_threshold or
                        np.argmax(prob_class[0, ii, :]) == (prob_class.shape[2] - 1)):
                    continue

                cls_name = self._class_mapping[np.argmax(prob_class[0, ii, :])]
                if cls_name not in bboxes:
                    bboxes[cls_name] = []
                    probs[cls_name] = []

                (x, y, w, h) = regions_of_interest[0, ii, :]

                cls_num = np.argmax(prob_class[0, ii, :])
                try:
                    (tx, ty, tw, th) = prob_regr[0, ii, 4 * cls_num:4 * (cls_num + 1)]
                    tx /= self._config.classifier_regr_std[0]
                    ty /= self._config.classifier_regr_std[1]
                    tw /= self._config.classifier_regr_std[2]
                    th /= self._config.classifier_regr_std[3]
                    x, y, w, h = roi_helpers.apply_regr(x, y, w, h, tx, ty, tw, th)
                except:
                    pass
                bboxes[cls_name].append([self._config.rpn_stride * x, self._config.rpn_stride * y,
                                         self._config.rpn_stride * (x + w), self._config.rpn_stride * (y + h)])
                probs[cls_name].append(np.max(prob_class[0, ii, :]))

        # Aggregate the detected bounding boxes into results for this image
        detected_bboxes = []
        for key in bboxes.keys():
            bbox = np.array(bboxes[key])

            new_boxes, new_probs = roi_helpers.non_max_suppression_fast(bbox, np.array(probs[key]), overlap_thresh=0.5)
            for jk in range(new_boxes.shape[0]):
                x1, y1, x2, y2 = new_boxes[jk, :]
                real_x1, real_y1, real_x2, real_y2 = get_real_coordinates(aspect_ratio, x1, y1, x2, y2)
                detected_bboxes.append(bbox_result.BoundingBox(
                    class_names=(key,),
                    confidence=new_probs[jk],
                    x=real_x1,
                    y=real_y1,
                    width=real_x2 - real_x1,
                    height=real_y2 - real_y1
                ))

        self._bounding_boxes[image.identifier] = detected_bboxes
        self._gt_bounding_boxes[image.identifier] = (
            bbox_result.BoundingBox(class_names=obj.class_names,
                                    confidence=1,
                                    x=obj.bounding_box[0],
                                    y=obj.bounding_box[1],
                                    width=obj.bounding_box[2],
                                    height=obj.bounding_box[3])
            for obj in image.metadata.labelled_objects
        )

    def finish_trial(self):
        """
        End the current trial, returning a trial result.
        Return none if no trial is started.
        :return:
        :rtype TrialResult:
        """
        # TODO: Maybe need to set the recursion limit?
        # sys.setrecursionlimit(self._old_recursion_limit)
        self._model_rpn = None
        self._model_classifier_only = None
        self._class_mapping = {}
        result = bbox_result.BoundingBoxResult(
            system_id=self.identifier,
            bounding_boxes=self._bounding_boxes,
            ground_truth_bounding_boxes=self._gt_bounding_boxes,
            sequence_type=self._sequence_type,
            system_settings=config_help.serialize_config(self._config)
        )
        self._bounding_boxes = None
        self._gt_bounding_boxes = None
        return result

    def validate(self):
        """
        Check that the system is valid.
        Checks that the weights file exists.
        :return: True iff the weights hdf5 file exists
        """
        return os.path.isfile(self._config.model_path)

    def serialize(self):
        """
        Serialize the entity to a dict format, that can be saved to the database
        :return: a dictionary representation of the entity, that can be saved to MongoDB
        """
        serialized = super().serialize()
        # restore original settings for serialization
        self._config.use_horizontal_flips = self._use_horizontal_flips
        self._config.use_vertical_flips = self._use_vertical_flips
        self._config.rot_90 = self._rot_90
        serialized['config'] = config_help.serialize_config(self._config)
        self._config.use_horizontal_flips = False
        self._config.use_vertical_flips = False
        self._config.rot_90 = False
        return serialized

    @classmethod
    def deserialize(cls, serialized_representation, db_client, **kwargs):
        """
        Deserialize an Entity as retrieved from the database
        Also accepts keyword arguments that are passed directly to the entity constructor,
        so that we can have required parameters to the initialization
        :param serialized_representation: dict
        :param db_client: DatabaseClient for deserializing child objects
        :return: An instance of the entity class, constructed from the serialized representation
        """
        if 'config' in serialized_representation:
            kwargs['config'] = config_help.deserialize_config(serialized_representation['config'])
        return super().deserialize(serialized_representation, db_client, **kwargs)


def format_img_size(img, config):
    """ formats the image size based on config """
    img_min_side = float(config.im_size)
    (height, width, _) = img.shape

    if width <= height:
        ratio = img_min_side / width
        new_height = int(ratio * height)
        new_width = int(img_min_side)
    else:
        ratio = img_min_side / height
        new_width = int(ratio * width)
        new_height = int(img_min_side)
    img = cv2.resize(img, (new_width, new_height), interpolation=cv2.INTER_CUBIC)
    return img, ratio


def format_img_channels(img, config):
    """ formats the image channels based on config """
    img = img[:, :, (2, 1, 0)]
    img = img.astype(np.float32)
    img[:, :, 0] -= config.img_channel_mean[0]
    img[:, :, 1] -= config.img_channel_mean[1]
    img[:, :, 2] -= config.img_channel_mean[2]
    img /= config.img_scaling_factor
    img = np.transpose(img, (2, 0, 1))
    img = np.expand_dims(img, axis=0)
    return img


def format_img(img, config):
    """ formats an image for model prediction based on config """
    img, ratio = format_img_size(img, config)
    img = format_img_channels(img, config)
    return img, ratio


def get_real_coordinates(ratio, x1, y1, x2, y2):
    """
    Method to transform the coordinates of the bounding box to its original size
    :param ratio:
    :param x1:
    :param y1:
    :param x2:
    :param y2:
    :return:
    """
    real_x1 = int(round(x1 // ratio))
    real_y1 = int(round(y1 // ratio))
    real_x2 = int(round(x2 // ratio))
    real_y2 = int(round(y2 // ratio))
    return real_x1, real_y1, real_x2, real_y2
